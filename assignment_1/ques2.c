#include<stdio.h>


int main()
{
    int num;
    printf("enter a number : "); 
    scanf("%d",&num);

    printf("The number in octal is %o\n",num); 
    printf("The number in hexadecimal is %x\n",num); 
    printf("The number in decimal is %d\n",num); 
    printf("The number as a character is %c\n",num);


	return 0;
}
